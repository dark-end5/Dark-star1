function convert(text, mapIndex) {
  const m = maps[mapIndex];
  if (!m) return text;
  if (m.special === 'strikethrough') {
    return text.split('').map(c => c + '\u0336').join('');
  }
  const fromArr = [...m.from];
  const toArr   = [...m.to];
  return [...text].map(ch => {
    const i = fromArr.indexOf(ch);
    return i >= 0 && toArr[i] ? toArr[i] : ch;
  }).join('');
}

